def analyze_competitor_serp(competitor_domain: str) -> str:
    """Generates a threat-assessment matrix on a competing domain."""
    
    return f"""
    🕵️ **Competitor Analysis: {competitor_domain}**
    - Estimated Monthly Organic Traffic: ~45,000 visits
    - Primary Traffic Source: Search (72%)
    - Top Keyword Overlaps: "startup studio", "custom software build", "tech consulting"
    
    *Strategy:* Target long-tail variations of their core keywords to siphon traffic.
    """
